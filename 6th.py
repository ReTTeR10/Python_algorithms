'''
6. В программе генерируется случайное целое число от 0 до 100. 
Пользователь должен его отгадать не более чем за 10 попыток. 
После каждой неудачной попытки должно сообщаться, больше или меньше загаданного введенное пользователем число. 
Если за 10 попыток число не отгадано, то вывести его.
'''


from random import randint as ri


num = ri(0, 100)
n = 10
while n != 0:

    ans = int(input('Введите число от 0 до 100: '))
    if ans == num:
        print(f'\nПоздравляю! Вы отгадали число {num}!')
        break

    else:
        if ans > num:
            print('\nМимо! Попробуйте еще раз.(меньше вашего)')
            n -= 1

        else:
            print('\nМимо! Попробуйте еще раз.(больше вашего)')
            n -= 1


if ans != num:
    print(f'Вы проиграли =(. Загаданное число: {num}')
